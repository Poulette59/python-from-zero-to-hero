# Python llama items a las parejas en un diccionario, mientras que en las listas son solo elementos
# Para agregar items a un diccionario simplemente de le asigna el valor a una nueva llave
english_to_spanish = {"hi":"hola", "bye": "chao"}

english_to_spanish["one"] = "uno"
english_to_spanish["two"] = "dos"

print(english_to_spanish)