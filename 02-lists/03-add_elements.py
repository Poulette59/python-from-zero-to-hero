# Para agregar elementos al final del arreglo utilizamos el método .append()

letters = ["a", "b", "c"]

letters.append("x")

print('leters:',letters)

# Para insertar elementos en una posición específica utilizamos el método .insert()

vowels = ["a", "e", "i", "u"]

# Vamos a insertar la o en la tercera posición

vowels.insert(3,'o')

print("vowels:", vowels)